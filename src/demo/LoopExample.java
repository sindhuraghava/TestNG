package demo;

public class LoopExample {

	public static void main(String[] args) {
		for(int i=1;i<4;i++) {
			System.out.println("the valus of i :"+i);
		}
		
		int i = 5;
		while(i>1) {
			System.out.println(" i value :"+i);
			i--;
		}
		
		int x =10;
		do {
			System.out.println(x);
			x++;
		}while(x>20);

	}

}
